#ifndef UTILS_H_
#define UTILS_H_


#include <string.h>


static inline
bool str_ends_with(const char *str, const char *end)
{
    if (strlen(str) < strlen(end))
    {
        return false;
    }
    if (strcmp(end, &str[strlen(str) - strlen(end)]) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
}


#endif /* UTILS_H_ */
